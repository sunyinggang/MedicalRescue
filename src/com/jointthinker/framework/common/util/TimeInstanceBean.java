package com.jointthinker.framework.common.util;

public class TimeInstanceBean {
	
	private Long timeinstanceid;
	
	private String amstart;
	
	private String amend;
	
	private String pmstart;
	
	private String pmend;
	
	public TimeInstanceBean() {}

	public Long getTimeinstanceid() {
		return timeinstanceid;
	}

	public void setTimeinstanceid(Long timeinstanceid) {
		this.timeinstanceid = timeinstanceid;
	}

	public String getAmstart() {
		return amstart;
	}

	public void setAmstart(String amstart) {
		this.amstart = amstart;
	}

	public String getAmend() {
		return amend;
	}

	public void setAmend(String amend) {
		this.amend = amend;
	}

	public String getPmstart() {
		return pmstart;
	}

	public void setPmstart(String pmstart) {
		this.pmstart = pmstart;
	}

	public String getPmend() {
		return pmend;
	}

	public void setPmend(String pmend) {
		this.pmend = pmend;
	}

	
}
